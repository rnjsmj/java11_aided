package org.kh.site;
//산술연산
public class Operation2 {

	public static void main(String[] args) {
		int num1 = 98;
		int num2 = 18;
		System.out.println("num1 + num2 = " + (num1+num2));
		System.out.println("num1 - num2 = " + (num1-num2));
		System.out.println("num1 * num2 = " + (num1*num2));
		System.out.println("num1 / num2 = " + (num1/num2));
		System.out.println("num1 % num2 = " + (num1%num2));

	}

}
